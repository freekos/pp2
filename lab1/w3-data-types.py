# str
str = "Hello World!"
print(type(str))

# int
int = 5
print(type(int))

# float
float = 5.0
print(type(float))

# complex
complex = 5 + 5j
print(type(complex))

# list
list = [1, 2, 3]
print(type(list))

# tuple
tuple = (1, 2, 3)
print(type(tuple))

# range
range = range(10)
print(type(range))

# dict
dict = {"name": "John", "age": 20}
print(type(dict))

# set
set = {1, 2, 3, 4, 5}
print(type(set))

# frozenset
frozenset = frozenset({1, 2, 3, 4, 5})
print(type(frozenset))

# bool
bool = True
print(type(bool))

# bytes
bytes = b"Hello World!"
print(type(bytes))
print(bytes)

# bytearray
bytearray = bytearray(b"Hello World!")
print(type(bytearray))
print(bytearray)

# memoryview
memoryview = memoryview(b"Hello World!")
print(type(memoryview))
print(memoryview)

# None
none = None
print(type(none))